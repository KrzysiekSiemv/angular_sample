export class Person {
    id: number;
    name: string;
    surname: string;
    job: string;
    age: number;
    constructor(id: number, name: string, surname: string, job: string, age: number){
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.job = job;
        this.age = age;
    }
}
